def _keyboard(active: str) -> InlineKeyboardMarkup:
    def label(base: str, page: str) -> str:
        return f"• {base} •" if page == active else base

    return InlineKeyboardMarkup(
        inline_keyboard=[
            [
                InlineKeyboardButton(text=label("📖 قوانین بازی", PAGE_GAME), callback_data=f"help:{PAGE_GAME}"),
                InlineKeyboardButton(text=label("🤖 راهنمای ربات", PAGE_BOT), callback_data=f"help:{PAGE_BOT}"),
                InlineKeyboardButton(text=label("✅ باید و نبایدها", PAGE_RULES), callback_data=f"help:{PAGE_RULES}"),
            ]
        ]
    )


@router.message(Command("راهنما"))
async def cmd_help(message: Message) -> None:
    await message.answer(
        _TEXT[PAGE_GAME], reply_markup=_keyboard(PAGE_GAME), parse_mode="HTML"
    )


@router.callback_query(F.data.startswith("help:"))
async def handle_help_page(callback: CallbackQuery) -> None:
    page = callback.data.split(":")[1]
    if page not in _TEXT:
        await callback.answer()
        return
    try:
        await callback.message.edit_text(
            _TEXT[page], reply_markup=_keyboard(page), parse_mode="HTML"
        )
    except Exception:
        pass
    await callback.answer()